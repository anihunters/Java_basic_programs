
public class Increment_decrement {

	public static void main(String[] args) {

		int a=10;
		System.out.println(a--); //10 ------>9....it will continue in next step
		System.out.println(--a); //9 ------->8
		System.out.println(a++); //8 ------->9
		System.out.println(++a); //10

		
	}

}
