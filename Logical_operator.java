
public class Logical_operator {

	public static void main(String[] args) {

		int a=10,b=20;
		System.out.println(a==b && a!=b); //In and operator both the condition should be true otherwise it will print false
		System.out.println(a==b || a!=b); // In or operator it will check both the condition then it will declare the result
		System.out.println(!(a>b)); //In not operator it will automatically reverse the condition because we started with not


		
	
	}

}
