
public class Operator_2 {

	public static void main(String[] args) {

		int a=10,b=20;
		System.out.println(a==b); //10 !=20
		System.out.println(a!=b);  //so on like this we are comparing the conditions
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);



	}

}
